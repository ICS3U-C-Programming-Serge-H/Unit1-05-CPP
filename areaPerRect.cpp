// This program calculates the area and perimeter of a rectangle
// with dimensions 3cm x 2cm

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions:" << std::endl;
    std::cout << "3cm x 2cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "Area is: " << (3 * 2) << "cm^2" << std::endl;
    std::cout << "Perimeter is: " << (2 * (3 + 2)) << "cm" << std::endl;
}
 